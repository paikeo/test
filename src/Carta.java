
public class Carta {

	private int valore;
	private String seme;

	public Carta(int valore, String seme) {
		super();
		this.valore = valore;
		this.seme = seme;
	}

	public int getValore() {
		return valore;
	}

	public void setValore(int valore) {
		this.valore = valore;
	}

	public String getSeme() {
		return seme;
	}

	public void setSeme(String seme) {
		this.seme = seme;
	}

}
